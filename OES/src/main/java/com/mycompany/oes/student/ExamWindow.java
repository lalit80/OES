        done = new int[noOfQuestions];
        data = new String[noOfQuestions][6];
        MyConnection connection = new MyConnection();
        ResultSet resultSet = connection.statement.executeQuery("select * from " + examName);

        int i = 0, j = 0;
        while(resultSet.next()) {
            for(j = 2; j < 8; ++j) {
                data[i][j - 2] = resultSet.getString(j);
            }
            ++i;
        }

        jTextArea1.setText(data[0][0]);
        jRadioButton1.setText(data[0][1]);
        jRadioButton2.setText(data[0][2]);
        jRadioButton3.setText(data[0][3]);
        jRadioButton4.setText(data[0][4]);

