    private void generic(ActionEvent evt) {
        currentQuestion = Integer.parseInt(evt.getActionCommand());
        jTextArea1.setText(data[currentQuestion - 1][0]);
        jRadioButton1.setText(data[currentQuestion - 1][1]);
        jRadioButton2.setText(data[currentQuestion - 1][2]);
        jRadioButton3.setText(data[currentQuestion - 1][3]);
        jRadioButton4.setText(data[currentQuestion - 1][4]);
        if(done[currentQuestion - 1] == 0) {
            buttonGroup.clearSelection();
        }
        else {
            buttonGroup.clearSelection();
            selectedButton[currentQuestion - 1].setSelected(true);
        }
    }

